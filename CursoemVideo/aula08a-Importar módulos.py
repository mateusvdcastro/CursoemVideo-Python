#maneira de importar uma parte especifica dentro de uma função
from math import sqrt,floor

num = int(input('Digite um número: '))
raiz = sqrt(num)
print(f'A raiz de {num} é igual a {floor(raiz)}')
# floor- Para arredondar o resultado da raiz

#maneira de importar a função inetira
import math
num1 = int(input('Digite um número: '))
raiz1= math.sqrt(num1)
print(f'A raiz de {num1} é igual a {math.floor(raiz1)}')
